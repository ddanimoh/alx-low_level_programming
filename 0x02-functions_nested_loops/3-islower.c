#include <stdio.h>
#include <ctype.h>
#include "main.h"

/**
 * islower - tests whether a character is a
 * lowercase letter from the English alphabet.
 * @c: character to test.
 * Return: 1 if it is a lowercase letter, 0 if it is not a lowercase letter.
 */
int islower(int c)
{
	if (islower(c))
		return (1);
	else
		return (0);
}
